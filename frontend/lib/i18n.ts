export function getTranslation(locale: Locale, key: string): string {
  return translations[locale]?.[key] ?? translations.en[key] ?? key;
}

export function createT(locale: Locale): (key: string) => string {
  return (key: string) => getTranslation(locale, key);
}

export function getStoredLocale(): Locale {
  if (typeof window === "undefined") return "en";
  const stored = localStorage.getItem("freezewise-locale");
  if (stored === "en" || stored === "ru" || stored === "cn") return stored;
  return "en";
}

export function setStoredLocale(locale: Locale): void {
  if (typeof window === "undefined") return;
  localStorage.setItem("freezewise-locale", locale);
}
